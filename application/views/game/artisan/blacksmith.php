<h2>鐵匠</h2>
<p class="quote">"有些傷疤是永遠都不會好的，而且也沒有必要再去戳舊傷，對嗎？"</p>
<p>在紛擾不休的聖休亞瑞世界裡，鑄造武器與防具的工作恰恰代表著失落。鐵匠的技藝被應用於各種用途。他們打造出來的東西可以用來屠殺敵人與守護人命，而這些兵器通常會和主人一同湮沒於沙場之中。</p>

<p>據說只有親自面對過死亡的人才能鑄造出戰爭的利器。如果傳言屬實，那麼粗獷的海德格‧埃蒙就是個天生的鐵匠。從他在卡爾蒂姆的悲慘童年開始，直到定居在受詛咒的新崔斯特姆，死亡始終伴隨著他。</p>

<p>恐怖的災厄侵襲海德格的城鎮，迫使他採取行動。儘管鍛造並非他的最愛，但眼看聖休亞瑞即將被邪惡吞噬殆盡的他，已不能再過著以往平凡的打鐵生涯。他會用他最擅長的技能來協助你；在漫長的冒險旅途中，專心為你打造對抗烈焰地獄的專屬利器。</p>