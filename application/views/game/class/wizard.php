	<h2>秘術師</h2>

	<p class="quote">“要是我的導師能夠看見現在的我...”</p>

	<p><img src="http://us.media.blizzard.com/d3/media/artwork/artwork-0072-large.jpg" alt="" class="class_art"></p>

	<p>來自魔法學院的魔法使用者遍佈整個聖休亞瑞 - 自仙塞島至卡爾蒂姆 - 擁有如〝法師〞〝戰巫〞等的綽號，但那些以秘術師自居的人，對他們其他施法者同伴來說簡直就是大巫見小巫。 不論是秘術師或是法師都掌控著秘法的神祕力量 - 那也是僅有的共同點。</p>

	<p>秘術師最為人知的幾種特質：叛逆和敏銳的感覺力，但同時也包括了他們高傲的滔滔不絕說著從魔法學院習得〝小心謹慎〞和〝安全〞的無聊理論。秘術師高人一等的態度來自他們與生俱來的天賦 - 將不羈的魔法力量操控自如 – 讓那力量透過孤獨、無需任何研究傳送給自己。任何因為沒有絕佳控制魔法而發生的意外都是種不幸...但，他鮮少會阻擋秘術師們投入不穩定的秘法之中。</p>

	<p>如果他們能夠完全掌控他們那兩面的天賦並避免自身受到毀滅，這個傲慢自負的秘法奇才職業或許終於能夠達到他們一直以來夢想的命運。</p>

