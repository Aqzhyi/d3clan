	<h2>野蠻人</h2>

	<p class="quote">“先祖庇護著我。”</p>

	<p><img class="class_art" src="http://us.media.blizzard.com/d3/media/artwork/artwork-0007-large.jpg" alt=""></p>

	<p>多年以來，居住在亞瑞特山附近的野蠻人，由於長年的生活方式使得他們如磐石般的強壯與堅毅。高大魁梧、生性殘暴的野蠻人把保衛亞瑞特山以及其中的神祕物體視為自己終生的職責。</p>

	<p>當他們的家園支離破碎後，許多野蠻人失去了信心。他們開始漫無目的的流浪，仍因過往而困擾，毫無榮譽或方向的四處遊蕩。</p>

	<p>但並不是所有的野蠻人都放棄了他們的使命。其中一些人仍惦記著過去那在酷寒與亞瑞特山巔四處覓食的日子。當同年紀的城市孩子仍抱著玩具時，他們就已經拿起利斧和長矛...失敗會讓他們在自己的族人面前無地自容。</p>

	<p>這些野蠻人在這個不同的世界中努力尋找著新的目標，任何試圖阻擋他們前進的人都將被無情的粉碎。</p>

