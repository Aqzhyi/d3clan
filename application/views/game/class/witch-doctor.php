	<h2>巫醫</h2>

	<p class="quote">“靈魂對我展露笑容。”</p>

	<p><img src="http://us.media.blizzard.com/d3/media/artwork/artwork-witch-doctor01-large.jpg" alt="" class="class_art"></p>

	<p>只有少數安巴魯一族中的男人和女人能被選中擔任巫醫一職，因為他們具有能夠看見並與靈魂溝通的獨特天賦。只有擁有與那些離開聖休亞瑞後便前往彼端世界 - 也稱之為無形之境中的幽魂互動的能力，才能成為巫醫。</p>

	<p>當一個人死亡後，巫醫能夠感覺到生命之息離開了冰冷的身體，他們能夠跟隨亡者的視線，注視著他們仍存於世的家人們。當災難或滅絕將數百個靈魂送往無形之境，將靈魂轉變為更邪惡的形體時，巫醫們則能在無盡間聽到靈魂們的顫慄與尖叫。</p>

	<p>巫醫永遠無法忽視靈魂的呼喊。當憂慮的聲音越漸越響時，他們就必須做出抉擇。選擇恢復自身世界與無形之境之間的平衡，又或是一輩子...甚至無盡的看著逝去的生靈受到折磨。</p>

