<h2>聖堂騎士</h2>
<p class="quote">「即使在最黑暗的地方，聖光依然指引著我們。」</p>

<p>聖堂騎士是為了正義而戰的神聖戰士，為了免除墜入邪道與惡魔附身的危險，他們捨棄了自我。他們保衛世界的決心並非空口白話：他們每個人都受過戰鬥訓練、也有過實戰經驗，憂鬱而專注的寇馬可正是其中的典範。</p>

<p>寇馬可在年輕的時候就來到了騎士團，並且很快被塑造成一名理想的聖堂騎士，自然而然地接受了禁慾的生活。他的信念無可動搖，以騎士團的標準來看更是如此；雖然他敬重實戰的能力，但他對於不符自己高道德標準的人仍表現出明顯的鄙視。</p>

<p>寇馬可的行事風格可能未必和你相同，但你們的目標是一致的，所以他會和你並肩作戰，消滅世間的邪惡，讓黑暗在至高的光明之下無所遁形。</p>