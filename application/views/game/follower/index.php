	</p>

	<p>追隨者如同玩家一樣，也能夠在不同等級時，解鎖一個接著一個的技能供玩家選擇；而且玩家不必擔心，此技能隨時都能夠透過追隨者互動「重新訓練」按鈕，來進行重新選擇。</p>

