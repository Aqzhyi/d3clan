	<h2>追隨者</h2>
	<p class="quote">只有傻瓜還有自以為「勇敢」的人才會獨自面對敵人。在保衛聖休亞瑞的這場戰爭裡，是誰會與你並肩作戰？</p>

	<p><img class="float_art" src="/static/img/game/follower/follower.png" alt=""></p>

	<p class="g_clear float_text">在《暗黑破壞神III》之中的追隨者，是玩家在旅路上的夥伴，跟隨著玩家與地獄惡魔戰鬥。玩過前作的朋友，應該知道前作有一個「傭兵」系統，玩家可以依喜好雇用傭兵來跟隨戰鬥；此系統在本作中與前作類似，只是形式不同而已。</p>

	<h2>如何使追隨者加入玩家？</h2>
	<p>如果你是才剛加入《暗黑破壞神III》這趟與地獄惡魔戰鬥的玩家朋友們，你應該還在《普通難度》之中。追隨者在遊戲一開始是不會立即加入玩家的，而是隨著玩家的遊戲進度，慢慢地加入玩家的旅程。</p>

	<p>玩家在遊戲的過程中，隨著故事發展，追隨者將會在主線任務中，加入玩家的隊伍，玩家無須擔心找不到追隨者。一旦追隨者在玩家的旅途中，加入了隊伍之後，追隨者將會停留在城鎮中，等待玩家的邀請。</p>

	<p class="alert alert-error">Tip: 追隨者必須在玩家處於「單人遊戲」時，追隨者才能加入玩家的隊伍。</p>

