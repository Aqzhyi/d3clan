	<h2>追隨者養成</h2>
	<p>
		追隨者雖然不是前作的傭兵，但玩家依然可以為追隨者裝備武器、防具及飾品；雖然追隨者不如玩家那樣擁有多樣化的裝備部位，但追隨者卻可以從裝備中，獲得數值的2.5倍加成。
	</p>

	<p>同時，如果追隨者所裝備的物品中，帶有「尋獲魔法物品機率」、「尋獲金幣機率」或「擊殺經驗加成」等屬性時，追隨者也會回饋上述屬性的25%至玩家身上。玩家直接打開面版就能看到加成完後的數值了。</p>

	<p class="img_show">
