	<h2>追隨者的機制</h2>
	<p>不同於前作，本作的追隨者是會隨著玩家一同升級的。具體的等級會落在比玩家低一個等級；假設玩家一直進行到角色60級，仍沒有使用過追隨者的話，追隨者的等級會自動爬升至59級。同時追隨者也不會降低玩家與惡魔戰鬥所獲得的經驗值。</p>

	<p>追隨者若是死亡，將會在原地蹲下，並且一小段時間之後，便會重新站起與玩家繼續協同作戰。玩家完全不必擔心追隨者死亡會有什麼副作用，僅僅是短暫的時間等待而已。當玩家拾獲「生命之球」的同時，追隨者的生命值也會回復，同樣地也不會影響玩家所應得的生命量。</p>

