	<h2>操作追隨者</h2>
	<p>追隨者在加入玩家的隊伍之後，玩家可以在遊戲介面的左上方看到追隨者的頭像。玩家可以透過右鍵點擊追隨者，便會出現一系列關於追隨者的操作。</p>

