					<span class="frame"></span>
				</span>

				<h3 class="category ">鐵匠</h3>
				海德格‧埃蒙， <strong>鐵匠</strong>
				，能夠打造出新的武器與防具，也能夠為多種物品打製鑲孔。他也可以將不要的魔法物品拆解為用來製作更強大裝備的一般材料和秘法材料。
