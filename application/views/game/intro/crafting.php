			<p>
				最常見的方式，就是透過拆解你不需要的物品來取得製作材料。當你獲得低品質並無法提昇你現有裝備的魔法物品時，你可以選擇出售給商人來換取金幣，或者你可以透過與鐵匠交談來拆解物品。只有魔法物品才可被拆解：低品質的普通物品不夠威力來產生製作材料。
			</p>

