			<p>擁有一點運氣和技巧，你將能撐過不只幾場戰鬥，然後發現自己尋找著如何替換身上磨損不堪或破舊的裝備的方法。非常明智。</p>
			<p>你可以向商人購買新物品。你可以從寶箱中或從腐爛骷髏手中（無論是死的還是活的）找到新物品。</p>
			<p>但是，藉由一些天賦異秉的盟友，你也可以製作新的物品和強化現有物品。</p>
