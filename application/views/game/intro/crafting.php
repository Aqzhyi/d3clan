		<div class="guide-section " id="training-your-artisans">
			<h2 class="header ">訓練你的工匠</h2>

			<div class="guide-image guide-image-right guide-align-right">
				<img src="http://tw.battle.net/d3/static/images/game/guide/crafting/training.jpg" alt="" />

			</div>

			<p>
				你也可以透過給予他們金幣來購買新的設備和進一步訓練他們的技能來提昇你的工匠。當獲得足夠的訓練後，他們將獲得等級提昇並解鎖製作更強的裝備的所需知識。
			</p>
			<p>
				你將在遊戲旅程中發現獨特的
				<strong>配方</strong>
				：能與你的工匠們分享的配方和設計圖。有些遊戲中最值錢和珍貴的物品必須透過此方式找到的設計圖來鑄造或設計而取得。
			</p>

