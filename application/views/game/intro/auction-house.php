				<li>
					當你點擊開始拍賣時，物品將立即自動於你的物品欄內移除並開始販售。如裝備之類的拍賣可於開始拍賣5分鐘內進行取消；商品類拍賣可隨時進行取消。所有拍賣期間為
					<strong>48小時</strong>
					，而每一個拍賣場中，你可同時擁有
					<strong>10筆</strong>
					的有效拍賣。
