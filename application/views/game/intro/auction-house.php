			<h2 class="header ">賣出</h2>

			<p>
				在任何人可於拍賣場購買物品前，要先有人將物品進行刊登賣出。你可能決定賣出你不需要的物品、多餘的製作材料或能夠以一個好價格賣出的特定稀有裝備。
			</p>
			<p>交易成功的拍賣將需支付15％的交易手續費。更多相關詳情，請參閱常見問題。</p>

