			<h2 class="header ">什麼是拍賣場？</h2>

			<p>
				目前台灣、香港和澳門的玩家將僅可使用遊戲金幣拍賣場，透過遊戲內金幣來進行購買和售出《暗黑破壞神III》冒險旅程中所獲得的物品。
			</p>

			<p>專家級模式中所創建的英雄角色將使用專有的〝僅限專家級模式〞的遊戲金幣拍賣場。這些角色無法使用一般的遊戲金幣拍賣場。</p>

		</div>

		<div class="guide-section " id="buying">
			<h2 class="header ">購買</h2>

			<p>《暗黑破壞神III》拍賣場讓玩家整裝自己的新角色、找尋特定配置的裝備、囤積消耗品或入手超級稀有的武器或防具。</p>
			<p>
				拍賣場中所賣出的物品大致上分為兩種一般類別： <strong>裝備</strong>
				- 如武器和防具與 <strong>商品</strong>
				- 如製作材料和寶石。拍賣場在處理這兩種類別物品拍賣時也有些許不同 - 查看常見問題來了解更多資訊。
			</p>

