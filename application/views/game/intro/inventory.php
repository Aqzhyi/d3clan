			</p>
			<p>
				商人可以在你等待更強大武裝的時候，幫助你暫時補足你的裝備需求。他們所販售的物品品質各有不同，並非每個商人都擁有你所屬職業和技能需要的完美物品，他們時常會有新的進貨，所以建議你最好定期探訪他們。
