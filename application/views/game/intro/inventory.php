			<p>
				當你拾取物品時，它會出現在你的 <strong>物品欄</strong>
				內，當你在遊玩《暗黑破壞神III》時獲得掉落物品時的置物空間。你可以透過點擊「預設熱鍵I」來查看你的物品欄。
			</p>
			<p>
				於物品欄頁面查看你的英雄圖像，你將會看到角色身體不同的部位擁有不同的裝備欄位（頭部、肩膀、頸部、身軀、腰部、腿部、腳部、手指和手）。
			</p>
			<p>
				要為你的英雄配戴你找到的裝備，只要左鍵點擊該物品並拖曳至對應的欄位（白色外框）即可。你也可以直接右鍵點擊該物品來將該物品自動裝備至正確的欄位。
			</p>
			<p>
				除了顯示你當前所使用的裝備外，你的物品欄也同時顯示你所攜帶但未裝備的物品。在你當前裝備的物品下方有一排方塊。當你拾取一件物品，它將會出現在方塊中。較小的物品，如藥水和戒指等會佔用一個物品欄中的方塊空間，如果是較大的物品，像是矛和盾，就會佔用多個空間。
			</p>
			<p>管理你的物品欄來避免空間不足的情況會是個好主意。</p>

