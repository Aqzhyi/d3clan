			<p>
				你的
				<strong>儲物箱</strong>
				，是個位於城鎮中的大型寶箱，是個你可以儲藏想要保留或暫時還不需要的裝備和其他物品的空間。保留你覺得以後可能會用得上或不確定是否想賣掉的物品會是比較理想的作法。
			</p>
			<p>
				你帳號中的所有角色皆共享你的儲物箱。這代表著當你正在玩野蠻人的時候，你發現某個物品可能比較適合巫醫，你可以將該物品存在儲物箱中，而當你以巫醫角色登入的時候，就可以取出使用。
			</p>
			<p>如果你需要更大的儲物箱空間，你可以於儲物箱視窗中購買更多空間。</p>

