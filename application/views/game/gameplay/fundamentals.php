				<p class="caption">提示：所有屬性，越高越好。</p>

			</div>

			<p>
				每個《暗黑破壞神 III》中的英雄都擁有極不可思議的力量、速度、堅毅或智慧，讓他們能夠挑戰黑暗勢力。這些核心特性就叫做
				<strong>屬性</strong>
				，代表一個英雄在其所專精的領域是多麼有力的數值。這裡是《暗黑破壞神 III》一些英雄的屬性簡單介紹：
			</p>
			<ul>
				<li>
					<strong>力量</strong>
					：野蠻人的主要屬性。力量提高野蠻人的攻擊傷害並提高所有職業的護甲值。
				</li>
				<li>
					<strong>敏捷</strong>
					：狩魔獵人和武僧的主要屬性。敏捷提高狩魔獵人和武僧的攻擊傷害並提高所有職業的閃躲機率。
				</li>
				<li>
					<strong>智力</strong>
					: 巫醫和秘術師的主要屬性。智力提高巫醫和秘術師的攻擊傷害並提高所有職業的抗性。
				</li>
				<li>
					<strong>體能</strong>
					: 提高生命值，在角色死亡前可承受的傷害量。
				</li>
				<li>
					<strong>護甲值</strong>
					：降低遭受來自敵人攻擊的傷害量。
				</li>
				<li>
					<strong>傷害</strong>
					: 顯示你的攻擊所造成的每秒傷害平均數值。這個屬性從你所裝備的武器、人物主要屬性、攻擊速度和爆擊機率中所獲得。
				</li>
			</ul>

			<p>每個職業都有這些屬性，但依照你個人喜愛的遊戲風格，你可能擁有較為偏好的屬性。</p>

			<p>
				舉例來說，如果你發現自己衝進和衝出戰鬥來發動快速的攻擊而不是與敵人面對面的戰鬥，那麼你可能需要著重於你的傷害值而非體能。若你不常承受傷害，那麼提高生命值就不是那麼的有用。
			</p>
			<p>
				屬性在你等級提昇的時候也會自動的提高。在你進行遊戲的同時，你也會變得越來越強大。另外，你找到的很多裝備，裝備後都能夠賦予屬性一些特定的額外加成；選擇可強化你最在乎的屬性的裝備。
			</p>
			<p>你可以從物品欄頁面隨時查看你的當前屬性（預設按鍵 I）</p>

