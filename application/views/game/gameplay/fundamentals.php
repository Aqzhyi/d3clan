			<h2 class="header ">戰鬥操作</h2>

			<p>
				戰鬥是《暗黑破壞神III》的主要核心。當你在聖休亞瑞各地冒險時，不論陰森恐怖的墓園、還是酷熱難當的沙漠和其他危險地區，隨時都會有怪物從暗處竄出、成群結隊衝向你，或是挖洞躲藏在你的腳下。你能聽見螢幕範圍外的怪物在黑暗中發出的聲音，而且就算你掉頭逃跑，許多怪物還是會繼續窮追不捨。
