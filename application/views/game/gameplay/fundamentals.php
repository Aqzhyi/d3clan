			<p>
				《暗黑破壞神III》並不會在你完成最終章節後就結束。在完成了《暗黑破壞神III》
				<strong>普通</strong>
				難度後。你將解鎖一個全新的難度等級：
				<strong>惡夢</strong>
				，來讓你繼續遊戲。如果你撐過了惡夢並完成了遊戲，你將解鎖更具有挑戰性的
				<strong>地獄</strong>
				難度。要是你打敗了地獄....那麼
				<strong>煉獄</strong>
				正等著你。
			</p>
			<p>
				你將能夠攜帶你的現有角色（包含所有該角色的等級、技能、力量和裝備）來開始新的遊戲難度 - 但那並不代表你就能夠到處肆虐無助的惡魔們。使用現有的角色來進行新的遊戲難度是 <i>有必要</i>
				的。你將需要任何能讓自己存活下來的一點優勢。
			</p>
			<p>
				在惡夢和地獄模式中（或以上）你將繼續獲得等級和技能符文，但你也同時能找到僅限這些難度獨有的物品、寶石、藥水、防具和武器。你將越漸越強大，但別忘了，你的敵人也是。
			</p>
			<p>
				當你達到60級後，擊敗某些特定頭目將能夠給予你一個叫做涅法雷姆之勇的暫時性能力加成效果。這個效果可維持30分鐘，死亡後仍將持續擁有，並最多可與其他涅法雷姆之勇增益疊加至5次。每個涅法雷姆之勇能讓魔寶尋獲率與金幣尋獲量提高15%。
			</p>
			<p>狩獵愉快！</p>

