				專家級英雄角色可使用專家級限定的遊戲金幣拍賣場，但不可以購買或出售物品給非專家級英雄角色。他們不可以使用現金拍賣場。專家級英雄角色僅可與你帳號中其他專家級英雄分享使用儲物箱中的物品。
			</p>
			<p>專家級模式不建議膽小的玩家嘗試。</p>

