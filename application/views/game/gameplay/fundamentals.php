				在攻擊怪物時，你可以在畫面上方看到怪物的名稱，以及代表怪物生命值的紅色長條，能夠清楚看出怪物還能承受多少打擊，然後才會劇烈爆炸死亡、或是倒在你腳下的那灘血水裡。
			</p>
			<p>
				當長條由滿的（紅色）變成空的（黑色），怪物就會死亡。（如果你看到怪物的上半身還在朝你爬過來、伸手要抓你的喉嚨，那就表示它還沒死透。）
			</p>

