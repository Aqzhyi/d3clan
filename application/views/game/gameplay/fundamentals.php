			<h2 class="header ">專家級模式</h2>

			<p>
				當任何《暗黑破壞神III》英雄達到10級後，你可在創建角色頁面來創建
				<strong>專家級模式</strong>
				的英雄。專家級英雄角色和普通角色一樣，從普通遊戲難度開始並也繼續其後的難度來遊玩《暗黑破壞神III》。專家級英雄與普通角色僅有一個最關鍵的不同之處：
			</p>
