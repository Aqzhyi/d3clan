		<div class="desc">
			追隨者是戰鬥經驗豐富老練的伙伴，能夠和你一同並肩作戰。他們會陪伴你離開安全的城鎮，攻擊所有威脅你的敵人、提高你的攻擊戰力。他們也能提供多種特殊的輔助能力，像是使敵人緩速、弱化、分神，或者是治療你。
