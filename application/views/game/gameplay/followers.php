				<p class="caption">從營地中召集你的追隨者。</p>

			</div>

			<p>
				追隨者會自動為你而戰，但你亦可選擇他們的技能來主導他們在戰鬥中的表現。舉例而言，如果你為聖堂騎士選用了「衝鋒」，他將會開始快速奔行衝向敵人，對其造成昏迷；而「治療」則能夠確保他在戰鬥過程中恢復你的生命值。
			</p>

			<p>
				沒有隨你出征的追隨者會留在營地，摩拳擦掌的等候你邀請他們加入戰鬥。只要走到他們身邊，點選他們，然後選擇「邀請」，他們就會加入（並且替換掉原有的追隨者）。
			</p>
			<p>你還可以點選追隨者，與他們交談，瞭解他們個人的故事與經歷，以及聽取他們對你當前任務的建議。</p>

		</div>

		<div class="guide-section " id="training-your-followers">
			<h2 class="header ">訓練追隨者</h2>

			<p>
				追隨者也會隨著你在《暗黑破壞神III》的歷練而成長，提高經驗與等級，解鎖新的技能（每5級），而且他們能裝備你找到或買到的裝備（但他們無法裝備一般防具，只能裝備特定的飾品、手持裝備和武器）。
			</p>

