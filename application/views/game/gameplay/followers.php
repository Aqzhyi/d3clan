			<p>
				要為追隨者進行強化，請先打開他們的人物視窗，點擊「預設熱鍵 F」，或以滑鼠點選畫面左上角他們的頭像，你會見到兩排技能列表。將滑鼠游標移動到技能圖示上即可查看其效用，然後再點選你想要他們使用的技能。你同樣可以於此選單中，透過右鍵點擊想要變更的追隨者技能來重新訓練追隨者的技能。
			</p>
			<p>你也能改變追隨者的「裝備」，從你的物品欄點選他們能裝備的物品，然後移動擺放到追隨者視窗裡對應的裝備欄位。</p>
			<p>
				選擇最符合你戰鬥風格的追隨者和技能，若是你喜歡親自在前線肉搏戰鬥、阻擋敵人，建議選擇能治療你或延緩敵人攻擊的追隨者。若是需要更高的殺傷力，則可為追隨者配上可造成持續性傷害、或是可攻擊多重目標的技能。
			</p>

