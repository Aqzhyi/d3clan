			<p>除了眾多遊盪於這片土地上不同種類的"普通"怪物以外，有時候你也將遇到一些特殊的怪物。</p>
			<ul>
				<li>
					<p>
						<strong class="d3-color-blue">勇士</strong>
						怪物的名字是顯示為藍色的。牠們以成群結隊的方式行動並且比一般的怪物要來的強大：牠們需要更多次攻擊才能被消滅、傷害更高並擁有
						<strong>附加效果</strong>
						的能力，例如能夠進行傳送來閃躲你的攻擊或在受到攻擊時釋出閃電。
					</p>
				</li>
				<li>
					<p>
						<strong class="d3-color-yellow">稀有</strong>
						怪物的名字是顯示為黃色的，稀有怪物的生命值也比同類的一般怪物要來的高，擁有特殊附加效果能力，通常陪同著3到4個較弱但也具有類似能力的爪牙。
					</p>
				</li>
				<li>
					<p>
						<strong class="d3-color-purple">獨特</strong>
						怪物的名字是顯示為紫色的，牠們通常比勇士怪物要來的強大：擁有更高的生命值、更高傷害並通常擁有多個能力。
					</p>
				</li>
			</ul>
			<p>
				稀有和獨特的怪物通常根據牠們擊殺艱難度給予同等的獎勵：更強大生命治癒力量的較大的生命之球、更多金幣和更多（通常更好）的物品。
			</p>
			<p>
				當遊戲進行到特定的階段，你將會發現自己遇到真正恐怖的
				<strong>頭目</strong>
				怪物：比任何其他怪巨大不然就是極為危險的敵人。你將會需要使出渾身解數和機智來擊敗這些怪物，然後， <i>如果</i>
				你能解決牠們，你將能贏得超棒的獎勵。
			</p>

