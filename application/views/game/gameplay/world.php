		<h2 class="header ">遊戲場景</h2>

		<div class="desc">
			<p>
				聖休亞瑞世界的景觀每次拜訪都會有所不同。那是因為遊戲絕大多數的場景，除了主要的幾個城鎮和關鍵地標之外，通通都是隨機生成的。它們就像拼圖一樣，共同組成一個個迷宮和地下城，在你每次玩的時候都不一樣。
			</p>
			<p>
				每當你開始一場新的《暗黑破壞神III》遊戲，或是讀取既有的人物進度時，你的世界都會隨之改變。新的不同怪物會從林葉下的暗處現身，寶箱也會補滿，出現在新的地方。而且連原本已經探索過的場景和地圖也會產生變化，洞穴和樹木挪到了別處，而原本走過的路卻可能會消失不見，或是轉而蜿蜒通向一窩危險的怪獸。甚至一些任務目標也改變了！
			</p>
