				<p class="caption">儲存點通常與新地區或任務目標有關。</p>

			</div>

			<p>
				《暗黑破壞神III》裡的隨機生成要素都是為了提高遊戲的耐玩度而設計的。隨機生成的遊戲世界也代表了你的遊戲過程是獨一無二的。
			</p>
			<p>
				你不用手動「儲存」遊戲才能延續遊戲的進度。你的遊戲狀態（包括你得到的經驗值、收集到的戰利品，還有你完成的任務）在你抵達儲存點的時候會自動儲存，在你玩夠了、從遊戲選單點擊「離開遊戲」或「退出遊戲」時也會儲存。
			</p>
			<p>
				如果你在身處地城或野外的時候結束遊戲，你讀取人物的時候不會從先前的地點出現。反之，你會從上一個儲存點，或最近的「安全」地點現身：通常是附近的城鎮或營地。要回到先前的戰鬥地點，你可以循原路跑回去，或者使用傳送平台。只要你抵達各章節的任一傳送平台，你就可以隨時傳送回該區域，所以這些平台對於你的遊戲進度是很重要的。
			</p>
			<p>你的探索可長可短、隨你喜好而定，可是你最好步步為營並且找出傳送平台，確保你可以在遊戲世界中有效率地旅行。</p>

		</div>

