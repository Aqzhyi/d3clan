			<p> <strong>聖壇</strong>
				是遭褻瀆的舊時神聖之地。不像其他世界中許多突然而來的驚喜，它們永遠都是令人愉悅的地方。
			</p>
			<p>
				當你點擊一個被褻瀆的聖壇，它將被淨化，而你（和你的盟友）將得到一個暫時性的賜福效果 - 你的能力獲得強化 - 持續短暫的一段時間。每一個你所發現的聖壇僅能給予其賜福效果一次。
			</p>
			<p>這裡是一些你將遇到的一些聖壇，包括它們的賜福：</p>
			<p>
				<ul>
					<li> <strong>啟迪聖壇：</strong>
						位於此聖壇範圍的玩家將獲得殺死怪物後所得到的經驗值提高10%的效果。
					</li>
					<li>
						<strong>狂怒聖壇：</strong>
						位於此聖壇範圍的玩家將獲得攻擊速度提高25%的效果。
					</li>
					<li>
						<strong>庇佑聖壇：</strong>
						位於此聖壇範圍的玩家將獲得所受傷害減少75%的效果。
					</li>
					<li>
						<strong>機運聖壇：</strong>
						位於此聖壇範圍的玩家將獲得魔寶尋獲率與金幣尋獲量提高25%的效果。
					</li>
				</ul>
			</p>

		</div>

