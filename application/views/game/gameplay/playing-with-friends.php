			<p>雖然你最多可以和另外三個人組成隊伍（總共四名玩家），可是你和其他人組隊的時候不能帶追隨者，不管隊伍裡有多少人都一樣。</p>
			<p>
				隊伍裡的每個玩家都共享他們目前所在遊戲的狀態。舉例來說，如果你的人物殺死了骷髏王，可是你剛加入了一場公開遊戲、而這個遊戲的目標是打倒骷髏王，那麼在這場遊戲中他就會復活，而且所有打倒他之後的重點劇情事件（新區域、特定的NPC等等）都無法進行。
			</p>
			<p>
				最重要的一點或許是玩家越多，就代表敵人也越強：隊伍裡每多一名玩家，烈焰地域的惡魔就更為壯大。小心：雖然你的身旁有數名英雄助陣，但是以前不費吹灰之力就能解決的敵人，組隊以後可能要花上一番功夫才能消滅。
