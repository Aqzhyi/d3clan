			<p>
				一路奮戰成群的邪惡怪物，絕對有東西是可以讓你炫耀的。想要炫耀你的成就（或是你敏銳的創作能力），你可以自定義你的戰旗，利用不同的底圖、徽記、顏色和更多，以視覺上的方式展現個人的功績。
			</p>
			<p>你可以在登入《暗黑破壞神III》後立刻從角色選擇頁面找到你的戰旗，點擊戰旗將前往戰旗設定頁面來個人化你的戰旗。</p>
			<p>
				從戰旗設定頁面，你可以用多種不同的形狀、底圖、徽記、徽飾和顏色來裝飾你的戰旗（點擊畫面右邊的選項）或點擊"隨機"按鈕來隨機選擇戰旗的樣式。不同的紋章和形狀都與遊戲內成就息息相關，當你完成更多成就，你將能夠以越來越不同的樣式訂製你的戰旗。
			</p>
			<p>
				在公開遊戲中，其他玩家將能看到你的戰旗，當你外出冒險時，戰旗將留在城鎮中，任何你隊伍中的成員可點擊你的戰旗來傳送到你的所在地。確保你的戰旗要看起來很炫！
			</p>

