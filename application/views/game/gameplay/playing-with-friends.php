			<p>
				遊戲中，你的 <strong>社交視窗</strong>
				（遊戲畫面中右下方角落的小人形圖示）是當遊玩《暗黑破壞神III》時，與其他玩家互動的途徑。只要一眼就可以知道你有多少個 <strong>好友</strong>
				正在線上。
			</p>
			<p>
				視窗開啟時（預設熱鍵為O），你將能在面板中間看到你的好友名單。你可以透過在顯示方塊中輸入好友的電子郵件或 BattleTag 來將好友加入名單，一旦你新增了一名好友，在此面板中，你將能夠看到他們的狀態（離線或在線），又或者他們正有空或忙碌。點擊一名好友的名字來選擇他們，然後點擊"聊天"按鈕來傳送一則訊息。你也可以在此視窗內點擊"邀請加入隊伍"按鈕邀請他們加入你的遊戲。
			</p>
			<p>在視窗的左上方角落的下拉式選單來設定你自己的狀態，讓其他玩家知道你是否正有空一同遊戲或正忙碌中！</p>

