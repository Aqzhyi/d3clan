				<p class="caption">若你可以與好友一同遊戲，何必獨自戰鬥？</p>

			</div>

			<p>和其他玩家同樂能獲得某些強大的優勢，不過這麼一來也會對遊戲造成某些顯著的影響。</p>
			<p>
				你在隊伍裡的時候，可以在畫面上方看到隊友的生命值以及目前的狀況。如果你想馬上加入戰鬥，在城鎮裡點擊他們的戰旗就能瞬間傳送到隊友目前的所在。最後，你可以和別人
				<strong>交換</strong>
				你所收集到的物品：用滑鼠右鍵點擊其他玩家的頭像並選擇「交易」，就會打開交易視窗，讓你們交換彼此找到的戰利品、強化自己的裝備收藏。
			</p>
			<p>
				組隊時的戰利品是獨立的：每個玩家都會從敵人的屍體或閃亮亮的寶箱裡拿到自己的一份物品與金幣，想「偷」拿別人的戰利品是不可能的。
