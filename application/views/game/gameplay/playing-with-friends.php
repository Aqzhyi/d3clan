		<div class="guide-subsection " id="inviting-friends">
			<h3 class="category ">邀請好友加入遊戲</h3>

			<p>
				如果你正自己一個人進行遊戲並想要邀請其他玩家加入你的遊戲，辦法很簡單。當你在體驗《暗黑破壞神III》時，你可以"ESC"開啟遊戲選單，選擇"公開遊戲"開放自己的遊戲給所有玩家。
			</p>
			<p>
				你也可以詢問你認識的玩家陪你進行任務。在遊戲內，開啟你的社交選單，選擇你想要邀請的好友，然後點擊"邀請加入隊伍"。如果他們也正在玩《暗黑破壞神III》，他們將收到邀請，如果好友接受了邀請，他們將進入你的遊戲。
			</p>

