		<div class="guide-section " id="achievements">
			<h2 class="header ">成就</h2>

			<div class="guide-image guide-image-right guide-align-right">
				<img src="http://tw.battle.net/d3/static/images/game/guide/playing-with-friends/achievements.jpg" alt="" />

				<p class="caption">你勝利的傳奇。</p>
			</div>

			<p>
				當你在《暗黑破壞神III》裡達成了特定的英雄事蹟，你得到的不只是金幣、寶藏，或是把妖魔鬼怪打得血肉橫飛的快感：你還會解開成就，這是一種衡量你遊戲技術的指標。
			</p>
			<p>
				成就通常在完成重要、獨特、具有挑戰性的目標時才會解鎖，像是用特定的手法殺死怪物、在高難度下完成一個章節或全破遊戲。為了紀念你的優秀表現，成就是有點數的，你每獲得一個成就都能拿到特定的點數。所以，你可以拿《暗黑破壞神III》裡所有挑戰的總和來衡量自己目前的進度。
			</p>

			<p>
				你可以在遊戲內的成就選單（預設熱鍵為Y）裡瀏覽完整的成就列表，看看你目前的成果、規劃未來的計畫。成就是跟著帳號走的，而不是個別人物：當你的某個人物解鎖了一項成就，在其他人物的成就選單裡也看得到。
			</p>
			<p>
				有些成就格外具有紀念性：當你解鎖這類的成就時，你會連帶獲得
				<strong>戰旗</strong>
				的底圖和徽示，你可以自訂這面旗幟，在遊戲中向其他玩家展示你的豐功偉業。
			</p>

